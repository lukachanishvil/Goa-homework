# მომხმარებლისგან სახელის შეტანა
name = input("შეიყვანეთ თქვენი სახელი: ")

# მომხმარებლისგან არჩევანის შეტანა
choice = input("შეიყვანეთ არჩევანი ('u' ან 'l'): ")

# არჩევანის მიხედვით მოქმედება
if choice == "u":
    print(name.upper())  # სახელი დიდი ასოებით
elif choice == "l":
    print(name.lower())  # სახელი პატარა ასოებით
else:
    print("wrong information")  # არასწორი ინფორმაცია
