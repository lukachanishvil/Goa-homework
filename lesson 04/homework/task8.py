# ცვლადი celsius, სადაც შევინახავთ მთელ რიცხვს
celsius = 25  # მაგალითად, 25 °C

# ცვლადი fahrenheit, სადაც გამოთვლილი იქნება ტემპერატურა ფარენჰეიტში
fahrenheit = (celsius * 9/5) + 32

# შედეგის გამოშვება
print(f"ტემპერატურა ცელსიუსში: {celsius}°C")
print(f"ტემპერატურა ფარენჰეიტში: {fahrenheit}°F")
