# + (დამატება)
print(5 + 3)  # 8
print(10 + 20) # 30
print(7 + 12)  # 19

# - (გამოყოფა)
print(10 - 3)  # 7
print(25 - 7)  # 18
print(15 - 10) # 5

# * (მრავლობა)
print(4 * 3)   # 12
print(5 * 6)   # 30
print(7 * 9)   # 63

# / (გაყოფა)
print(10 / 2)  # 5.0
print(20 / 5)  # 4.0
print(15 / 3)  # 5.0

# % (ნაშთიანი გაყოფა)
print(10 % 3)  # 1
print(20 % 6)  # 2
print(25 % 7)  # 4

# ** (წოდება)
print(2 ** 3)  # 8
print(5 ** 2)  # 25
print(3 ** 4)  # 81
