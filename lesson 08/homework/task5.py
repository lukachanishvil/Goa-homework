# ცვლადების შექმნა და რიცხვების შენახვა
num1 = 10
num2 = 5
num3 = 3
num4 = 7
num5 = 2

# სხვადასხვა მათემატიკური ოპერაციები
print((num1 + num2) / num3 - num4 * num5 + num1 ** num3 % num2)  # პირველი მაგალითი
print(num1 * num2 + num3 - num4 / num5)  # მეორე მაგალითი
print(num1 ** num2 + num3 - num4 * num5)  # მესამე მაგალითი
print((num1 + num2) * (num3 - num4) / num5)  # მეოთხე მაგალითი
print(num1 - num2 + num3 * num4 % num5)  # მეხუთე მაგალითი
print(num1 / num2 + num3 * num4 - num5 ** num2)  # მეექვსე მაგალითი
print((num1 ** num2 + num3) * num4 - num5)  # მეშვიდე მაგალითი
print(num1 + num2 * num3 - num4 / num5)  # მერვე მაგალითი
print((num1 % num2) + (num4 - num5) * num3)  # მეცხრე მაგალითი
print(num1 * num2 ** num3 - num4 + num5)  # მეათე მაგალითი
