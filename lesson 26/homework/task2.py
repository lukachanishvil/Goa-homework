def sum_of_two_numbers(a, b):
    return a + b

# მაგალითის გამოყენება:
result = sum_of_two_numbers(5, 7)
print(f"ჯამი არის: {result}")  # შედეგი: ჯამი არის: 12

print(sum_of_two_numbers(10, 20))  # შედეგი: 30
print(sum_of_two_numbers(-5, 15))  # შედეგი: 10
print(sum_of_two_numbers(0, 0))    # შედეგი: 0
print(sum_of_two_numbers(1.5, 2.3))  # შედეგი: 3.8
