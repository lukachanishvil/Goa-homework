def reverse_string(input_string):
    return input_string[::-1]

# ფუნქციის გამოძახების მაგალითები:
print(reverse_string("გამარჯობა"))  # შედეგი: აბოჯრამაგ
print(reverse_string("Python"))      # შედეგი: nohtyP
print(reverse_string("12345"))       # შედეგი: 54321
print(reverse_string("კოდი"))        # შედეგი: იდოკ
print(reverse_string(""))            # შედეგი: (ცარიელი სტრიქონი)
