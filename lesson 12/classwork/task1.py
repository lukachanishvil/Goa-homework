# და ოპერატორი (and)
print(True and True)   # True: ორივე პირობა სიმართლეა
print(True and False)  # False: ერთი პირობა მცდარია
print(False and True)  # False: ერთი პირობა მცდარია
print(False and False) # False: ორივე პირობა მცდარია

# ან ოპერატორი (or)
print(True or True)    # True: ერთი პირობა მაინც სიმართლეა
print(True or False)   # True: ერთი პირობა მაინც სიმართლეა
print(False or True)   # True: ერთი პირობა მაინც სიმართლეა
print(False or False)  # False: არც ერთი პირობა სიმართლეა
