# ორი ბულეანური მნიშვნელობა
bool1 = True
bool2 = False

# and ოპერატორი
print(bool1 and bool2)  # შედეგი იქნება False, რადგან მეორე პირობა False-ია

# or ოპერატორი
print(bool1 or bool2)   # შედეგი იქნება True, რადგან პირველი პირობა True-ია

# სამი ბულეანური მნიშვნელობა
bool3 = True

# and ოპერატორი სამი ბულეანით
print(bool1 and bool2 and bool3)  # შედეგი იქნება False, რადგან მეორე პირობა False-ია

# or ოპერატორი სამი ბულეანით
print(bool1 or bool2 or bool3)   # შედეგი იქნება True, რადგან პირველი და მესამე პირობა True-ია
