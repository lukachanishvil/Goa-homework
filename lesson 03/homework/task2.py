#Front-end:
#Front-end არის ვებსაიტის ის ნაწილი, რასაც მომხმარებელი ხედავს და იყენებს. ის მოიცავს დიზაინსა და ინტერაქტიულ ელემენტებს, როგორიცაა ღილაკები, სურათები და ფორმები.

#Back-end:
#Back-end არის ვებსაიტის "შიდა" ნაწილი, რომელიც უზრუნველყოფს სერვერის მუშაობას, მონაცემთა დამუშავებასა და შენახვას.

#Full-stack:
#Full-stack დეველოპერი მუშაობს როგორც front-end-ის, ასევე back-end-ის ნაწილზე და ფლობს ორივეს განვითარების უნარებს.

