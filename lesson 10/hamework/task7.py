# ასაკების მიღება სამივე პირისგან
age1 = int(input("გთხოვთ, შეიყვანეთ პირველი პირის ასაკი: "))
age2 = int(input("გთხოვთ, შეიყვანეთ მეორე პირის ასაკი: "))
age3 = int(input("გთხოვთ, შეიყვანეთ მესამე პირის ასაკი: "))

# ასაკების ჯამი
total_age = age1 + age2 + age3

# საშუალო არითმეტიკული (ასაკების ჯამი / 3)
average_age = total_age / 3

# შედეგის დაბეჭდვა
print(f"სამი პირის ასაკების საშუალო არითმეტიკული არის: {average_age}")
